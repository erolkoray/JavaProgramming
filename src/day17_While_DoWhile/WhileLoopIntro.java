package day17_While_DoWhile;

public class WhileLoopIntro {

    public static void main(String[] args) {


        if(true){ // gets executed once
            System.out.println("Wooden Spoon");
        }

        System.out.println("-----------------------------------------");

        while(true){ // repeated if - gets executed multiple times (until condition becomes false)
            System.out.println("Wooden Spoon"); //
        }


    }
}
